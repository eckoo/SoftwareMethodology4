package DonutsAndCoffee;

public enum DonutFlavors {
	
	//These are the Yeast Donut flavors of the day.
	/**
	 * Creates a Regular yeast donut flavor.
	 */
	REGULAR,
	
	/**
	 * Creates a Chocolate yeast donut flavor.
	 */
	CHOCOLATE,
	
	/**
	 * Creates a Cranberry yeast donut flavor.
	 */
	CRANBERRY,
	
	//These are the Cake Donut flavors of the day.
	/**
	 * Creates a Pumpkin cake donut flavor.
	 */
	PUMPKIN,
	
	/**
	 * Creates a Cream cake donut flavor.
	 */
	CREAM,
	
	/**
	 * Creates a Coconut cake donut flavor.
	 */
	COCONUT,
	
	//These are the Donut Hole flavors of the day.
	/**
	 * Create a Jelly donut hole flavor.
	 */
	JELLY,
	
	/**
	 * Creates a Chocolate Glazed donut hole flavor.
	 */
	CHOCOLATE_GLAZED,
	
	/**
	 * Creates a Raspberry donut hole flavor.
	 */
	RASPBERRY
}
